/*Ốc sên

Description
Sau một quãng thời gian hẹn hò dù sao cũng phải có hồi kết – có ai mà lại không thích một cái kết có hậu một cái kết ngập tràn hạnh phúc một cái kết như trong films – nhưng thôi , tác giả cũng cố vun đắp cho hai bạn.

Tiếp tục câu chuyện: ” Nz và Yangz sau thử thách 1 cuối cùng hai bạn cũng đã được gặp nhau , bắt đầu tạo dựng một mối quan hệ, thân thiết hơn , đi chơi dong duổi với nhau cùng 2 chiếc vỏ ốc , lâu dần tình cảm hai bên cũng nảy nở và đâm chồi nảy lộc . Nhưng không - mọi thứ trên đời không chỉ toàn một màu hồng sau khi bố mẹ hai bên biết tin đều nhiệt liệt phản đối và không cho 2 bạn đến với nhau bởi vì nhà hai gia đình quá xa. Tuy mọi chuyện căng thẳng là như vậy nhưng hai bạn đều cố gắng vượt qua cùng nhau , lâu dần gia đình hai bên cũng mệt và không còn sức đâu để quan tâm, giờ đây hai bên gia đình lo tới chuyện cưới xin của Yangz và Nz”

Chúng ta sẽ đi tới với thử thách thứ hai :

“xác định xem những vị trí nào hợp lý để có thể làm tiệc cưới cho cả hai gia đình”

Vị trí hợp lý là vị trí nằm trong tam giác tạo bởi 3 tọa độ (x1;y1) nhà gia đình Nz, tọa độ (x2;y2) gia đình Yangz, và tọa độ (x3;y3) nhà của đôi bạn trẻ Yangz-Nz.

Input
Nhập vào 3 tọa độ (xi;yi) lần lượt là tọa độ của gia đình Nz, gia đình Yangz, và nhà của Yangz-Nz (-100 < xi, yi<100);
Nhập vào T là số địa điểm cần kiểm tra
T dòng tiếp theo mỗi dòng là một tọa độ (xi;yi) để kiểm tra xem tại đây có thực hiện hôn lễ được không

Output
Với mỗi địa điểm T in ra ”YES” nếu là vị trí hợp lý hoặc ”NO” nếu vị trí không hợp lý.*/
